function TodaysTasks() {
  const [tasks, setTasks] = useState(DEMO_TASKS);

  const toggle = (id: string) => {
    setTasks((ts) => ts.map((t) =>
      t.id === id ? { ...t, status: t.status === "completed" ? "pending" : "completed" } : t
    ));
  };

  const priorityColor: Record<string, string> = {
    critical: "#ef4444", high: "#f59e0b", medium: "#06b6d4", low: "#10b981"
  };

  return (
    <motion.div
      initial={{ opacity: 0, y: 20 }}
      animate={{ opacity: 1, y: 0 }}
      transition={{ delay: 0.2 }}
      className="card-base col-span-1 md:col-span-2"
    >
      <div className="flex items-center justify-between mb-5">
        <h2 className="font-display font-bold text-white">Today&apos;s Tasks</h2>
        <span className="text-slate-500 text-xs">{tasks.filter(t => t.status === "completed").length}/{tasks.length} done</span>
      </div>

      {/* Progress bar */}
      <div className="progress-track mb-4">
        <div className="progress-fill transition-all duration-500"
          style={{ width: `${(tasks.filter(t => t.status === "completed").length / tasks.length) * 100}%` }} />
      </div>

      <div className="flex flex-col gap-3">
        {tasks.map((task) => (
          <div
            key={task.id}
            onClick={() => toggle(task.id)}
            className={`flex items-start gap-3 p-3 rounded-xl cursor-pointer transition-all duration-200 ${
              task.status === "completed" ? "opacity-50" : "hover:bg-white/5"
            }`}
          >
            <div className="mt-0.5 flex-shrink-0">
              {task.status === "completed"
                ? <CheckCircle2 size={18} className="text-emerald-400" />
                : task.status === "in-progress"
                  ? <div className="w-4.5 h-4.5 w-[18px] h-[18px] rounded-full border-2 border-violet-400 border-t-transparent animate-spin" />
                  : <Circle size={18} className="text-slate-600" />
              }
            </div>
            <div className="flex-1 min-w-0">
              <div className={`text-sm font-medium ${task.status === "completed" ? "line-through text-slate-500" : "text-white"}`}>
                {task.title}
              </div>
              <div className="flex items-center gap-2 mt-1">
                <span className="text-[10px] px-2 py-0.5 rounded-full font-semibold"
                  style={{ background: `${priorityColor[task.priority]}15`, color: priorityColor[task.priority] }}>
                  {task.priority}
                </span>
                <span className="text-slate-600 text-[10px]">⏱ {task.duration}min</span>
                <span className="text-slate-600 text-[10px]">{task.category}</span>
              </div>
            </div>
          </div>
        ))}
      </div>
    </motion.div>
  );
}

