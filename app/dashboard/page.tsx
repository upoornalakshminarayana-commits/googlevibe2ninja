function AIBriefingCard() {
  const [typing, setTyping] = useState(true);
  const briefing = `Good morning, Arjun! 🌅 Today is critical — you have 4 high-priority tasks. Your JEE Mathematics syllabus is 3 weeks behind. The Risk Prediction Agent recommends spending 3h on Integration today. Your success probability is 72% — let's push it to 80% this week! You're on a 14-day streak. Don't break it! 💪`;
  const [displayed, setDisplayed] = useState("");

  useEffect(() => {
    let i = 0;
    const timer = setInterval(() => {
      if (i < briefing.length) {
        setDisplayed(briefing.slice(0, i + 1));
        i++;
      } else {
        setTyping(false);
        clearInterval(timer);
      }
    }, 18);
    return () => clearInterval(timer);
  }, []);

  return (
    <motion.div
      initial={{ opacity: 0, y: 20 }}
      animate={{ opacity: 1, y: 0 }}
      className="col-span-full glass-violet rounded-2xl p-6"
    >
      <div className="flex items-center gap-3 mb-4">
        <div className="w-9 h-9 rounded-xl flex items-center justify-center agent-active"
          style={{ background: "linear-gradient(135deg, #7c15f0, #a855f7)" }}>
          <Brain size={18} className="text-white" />
        </div>
        <div>
          <div className="font-display font-bold text-white text-sm">AI Morning Briefing</div>
          <div className="text-slate-500 text-xs">Generated by Gemini 2.5 Pro · just now</div>
        </div>
        <div className="ml-auto badge-violet text-xs">
          <Zap size={10} className="animate-pulse" />
          Live
        </div>
      </div>
      <p className="text-slate-300 text-sm leading-relaxed">
        {displayed}
        {typing && <span className="inline-block w-0.5 h-4 bg-violet-400 animate-pulse ml-0.5 align-middle" />}
      </p>
    </motion.div>
  );
}

